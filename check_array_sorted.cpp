class Solution {
  public:
    bool arraySortedOrNot(int arr[], int n) {
        // code here
        stack<int> s;
        s.push(arr[0]);
        
        for(int i=1; i<n; i++)
        {
            
            if(s.top()>arr[i]){
                return false;
            }
            else s.push(arr[i]);
        }
        
        return true;
    }
};

